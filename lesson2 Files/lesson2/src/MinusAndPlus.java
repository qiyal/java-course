public class MinusAndPlus {
    public static void main(String[] args) {
        int a = 1;
        // -1

        double d = 98.34;

        long l = -1;
        float f = -4.5f;

        System.out.println(d * -1);
        System.out.println(-a);

        System.out.println(-f);
        System.out.println(-l);


    }
}
